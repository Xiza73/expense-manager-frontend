interface HourResult {
  id: HourKey;
  hour: string;
}

export const getHours = (
  start: Hour,
  end: Hour,
  format: Format,
): HourResult[] => {
  const objArr = Object.values(Hour);
  let started = false;
  const hours: HourResult[] = [];

  for (const hour of objArr) {
    if (started) {
      hours.push({
        id: hour,
        hour: format === Format.HOUR_12 ? Hour12[hour] : hour,
      });

      if (hour === end) break;
      continue;
    }

    if (hour === start) {
      started = true;

      hours.push({
        id: hour,
        hour: format === Format.HOUR_12 ? Hour12[hour] : hour,
      });
    }
  }

  return hours;
};
